#include "3-calc.h"

/**
* op_add - adds stuff
* @a: an int. the first number
* @b: an int. the second number
* Return: Returns maff
*/

int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
* op_sub - subtraction things
* @a: an int. the first number
* @b: an int. the second number
* Return: Returns maff
*/

int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
* op_mul - times stuff
* @a: an int. the first number
* @b: an int. the second number
* Return: Returns maff
*/

int op_mul(int a, int b)
{
	return ((a) * (b));
}

/**
* op_div - divided stuff
* @a: an int. the first number
* @b: an int. the second number
* Return: Returns maff
*/

int op_div(int a, int b)
{
	return ((a) / (b));
}

/**
* op_mod - percentage things
* @a: an int. the first number
* @b: an int. the second number
* Return: Returns maff
*/

int op_mod(int a, int b)
{
	return ((a) % (b));
}
